using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

class Coordenada
{
    private double x;
    private double y;

    public void setX(double x)
    {
        this.x = x;
    }
    public double getX()
    {
        return this.x;
    }

    public void setY(double y)
    {
        this.y = y;
    }
    public double getY()
    {
        return this.y;
    }

}
